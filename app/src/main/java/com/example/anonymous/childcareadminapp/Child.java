public class Child {

    public String Fullname, Nickname, Age, Nationality, Religion, Fatheremail, Motheremail;

    public Child (){

    }

    public Child (String fullname, String nickname, String age, String nationality,
                  String religion, String fatheremail, String motheremail){
        this.Fullname = fullname;
        this.Nickname = nickname;
        this.Age = age;
        this.Nationality = nationality;
        this.Religion = religion;
        this.Fatheremail = fatheremail;
        this.Motheremail = motheremail;
    }
}


